# 변수이름 = 데이터
# = 할당 오른쪽에 있는 데이터를 변수에 저장한다

# 마스터이 챔피언 데이터를 변수에 저장
name = "마스터이"
level = 5
health = 800
attack = 90
print(name, level, health, attack)

# 변수에 저장된 데이터를 변경하기
level = 6
health = 900
attack = 100
print(name, level, health, attack)
level = level + 1
health = health + 100
attack = attack + 10
print(name, level, health, attack)

